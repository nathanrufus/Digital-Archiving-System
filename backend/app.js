// Create Express app
const app = express();

// Middleware
app.use(express.json());

// Root route
app.get('/', (req, res) => {
  res.send('Towfiq Logistics Digital Archiving Backend is running...');
});

// Start server
const PORT = process.env.PORT || 5000;
